  ,            _         , __
 /|   /       | |       /|/  \\o
  |__/        | |  _     | __/    _  _    __,
  | \\   |   | |/  |/     |   \\|  / |/ |  /  |
  |  \\_/ \\_/|/|__/|__/   |(__/|_/  |  |_/\\_/|/
           /|                              /|
           \\|                              \\|

