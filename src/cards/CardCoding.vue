    <FloatingPanel
        :left="850"
        :top="120"
        title="代码"
        :width="1000"
        background-color="black"
        animate-in-class="animate__animated"
    >
        <pre class="code">{{showingCode}}</pre>
    </FloatingPanel>
</template>

<script>
import FloatingPanel from "@/components/FloatingPanel";
export default {
    name: "CardCoding",
    components: {FloatingPanel},

    data(){
        return {
            showingCode: '',
            // ASCII 颜文字生成可以去这个网站： https://patorjk.com/software/taag/#p=display&h=1&f=Doom&t=Kyle%20Bing
            codeOrigin: `SCRIPT INIT v1.13.67
